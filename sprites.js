// Enhanced pixel drawing helper
class PixelCanvas {
    constructor(width, height) {
        this.width = width;
        this.height = height;
        this.canvas = document.createElement('canvas');
        this.canvas.width = width;
        this.canvas.height = height;
        this.ctx = this.canvas.getContext('2d');
        this.ctx.imageSmoothingEnabled = false;
    }

    clear() {
        this.ctx.clearRect(0, 0, this.width, this.height);
    }

    pixel(x, y, color) {
        if (x < 0 || x >= this.width || y < 0 || y >= this.height) return;
        this.ctx.fillStyle = color;
        this.ctx.fillRect(Math.floor(x), Math.floor(y), 1, 1);
    }

    rect(x, y, w, h, color) {
        this.ctx.fillStyle = color;
        this.ctx.fillRect(Math.floor(x), Math.floor(y), w, h);
    }

    // Draw sprite from 2D array with palette
    // 0 = transparent, 1-5 = palette indices
    drawSprite(x, y, data, palette, flipX = false) {
        if (!data || !palette) return;

        const spriteHeight = data.length;
        const spriteWidth = data[0] ? data[0].length : 0;

        for (let py = 0; py < spriteHeight; py++) {
            for (let px = 0; px < spriteWidth; px++) {
                const colorIndex = data[py][px];
                if (colorIndex > 0 && palette[colorIndex - 1]) {
                    const drawX = flipX ? x + (spriteWidth - 1 - px) : x + px;
                    this.pixel(drawX, y + py, palette[colorIndex - 1]);
                }
            }
        }
    }

    // Draw with alpha blending
    pixelAlpha(x, y, color, alpha) {
        if (x < 0 || x >= this.width || y < 0 || y >= this.height) return;
        this.ctx.globalAlpha = alpha;
        this.ctx.fillStyle = color;
        this.ctx.fillRect(Math.floor(x), Math.floor(y), 1, 1);
        this.ctx.globalAlpha = 1;
    }

    // Draw ellipse for shadow
    ellipse(cx, cy, rx, ry, color) {
        for (let y = -ry; y <= ry; y++) {
            for (let x = -rx; x <= rx; x++) {
                if ((x*x)/(rx*rx) + (y*y)/(ry*ry) <= 1) {
                    this.pixel(cx + x, cy + y, color);
                }
            }
        }
    }
}

